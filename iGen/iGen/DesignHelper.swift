import Foundation
import UIKit

class DesignHelper{
    class func colorDarkestBlue() -> UIColor{
        // COLOR 1B4965
        return UIColor(red: 27/255, green: 73/255, blue: 101/255, alpha: 1.0)
    }
    
    class func colorDarkBlue() -> UIColor{
        // COLOR 5DBCD2
        return UIColor(red: 93/255, green: 188/255, blue: 210/255, alpha: 1.0)
    }
}
